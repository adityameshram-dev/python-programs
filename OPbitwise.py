# Python Bitwise Operator

# Bitwise AND
a = 60 & 30
print(a)

# Bitwise OR
a = 60 | 30
print(a)

# Bitwise NOT
a=~10
print(a)

#  Bitwise XOR (Exclusive OR)
a = 60 ^ 30
print(a)

# Bitwise Left Shift
a = 2 << 2
print(a)

# Bitwise Right Shift
a = 2 >> 2
print(a)

# Used to to perform opertion on binary numbers