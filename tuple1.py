a = (5,4,3,2,1)

# function
print(min(a))  # it return minimum value from tuple
print(max(a))  # it return maxmimum value from tuple
print(sum(a))  # it return sum of all value from tuple
print(sorted(a)) # it return sorted tuple

# method
print(a.count(5))  # it return 5 how many time in tuple
print(a.index(3))  # it return index of 3