# Python Arithmetic Operator

a = 5
b = 5

print("Addition " ,a+b)        
print("Substraction " ,a-b)    
print("Multipliation " ,a*b)   
print("Division " ,format(a/b,".f")) # to Control on point value we use format function 
print("Module " ,a%b)          
print("Power a^b " ,a**b)    
print("Division ",a//b)      
