# Python Assignment Operator
a = 6

print(a) 

a+=6                # a=a+6 both are same
print(a) 

a-=6                # a=a-6 both are same
print(a) 

a*=6                # a=a*6 both are same
print(a) 

a/=6                # a=a/6 both are same
print(a) 

a**=6               # a=a**6 both are same
print(a) 