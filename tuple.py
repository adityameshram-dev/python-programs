#Python Tuple

tp = (1,2,3,4,5,)

print(tp)        # print values from list    

print(len(tp))   #  print how many values in list

print(tp[0])     # first item has index 0 up to n-1 


# we can perform more operation like membership etc     
if 5 in tp:
    print("5 present in the list")
else:
    print("5 not present i list")


# Tuple is data structure in python
# Tuple are ordered in python
# Tuple are immutable in python
# Tuple allow to store duplicate value
# Tuple allow to store heterogeneous data
# Tuple data accesssed by their index
# Tuple 1st item has 0 index
# Tuple allow negative index eg. list[-1] means last item
# Tuples are hashable (can be dictionary keys)
# Tuples have fixed size (cannot add/remove items)
