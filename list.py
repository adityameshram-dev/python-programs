print(mylist)      # print values from list 

print(len(mylist)) # print how many values in list

print(mylist[0])    # it print 1st element of list

# we can perform more operation like membership etc     
if 'apple' in mylist:
    print("apple word present in the list")
else:
    print("Apple word not present i list")


